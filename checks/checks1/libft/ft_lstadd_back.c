#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*current;
	t_list	*list;

	current = new;
	if (*alst == NULL)
		*alst = current;
	else
	{
		list = *alst;
		while (list->next != NULL)
			list = list->next;
		list->next = current;
	}
}
