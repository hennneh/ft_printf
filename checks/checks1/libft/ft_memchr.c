#include "libft.h"

void	*ft_memchr(const void *s1, int c, size_t n)
{
	size_t	i;

	if (!s1)
		return (NULL);
	i = 0;
	while (i < n)
	{
		if (((unsigned char *)(s1))[i] == (unsigned char)(c))
			return ((void *)(s1 + i));
		i++;
	}
	return (NULL);
}
