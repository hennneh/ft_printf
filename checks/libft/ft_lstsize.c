#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		len;
	t_list	*list;

	len = 0;
	if (lst)
	{
		list = lst;
		while (list != NULL)
		{
			list = list->next;
			len++;
		}
	}
	return (len);
}
